package Retangulo;

public class Retangulo {

	public static void main(String[] args) {
		
		int base;
		int altura;
		int area;
		int perimetro;
		
		base = 8;
		altura = 5;
		area = base * altura;
		perimetro = (base * 2) + (altura * 2);
		
		//System.out.print("O resultado da área é: ");
		//System.out.println(area);
		System.out.println("O resultado da área: " + area + " metros");
		System.out.print("O resultado do perímetro é: " + perimetro + " metros");
		
        
	}

}
